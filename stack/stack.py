class stack:

    def __init__(self):
        self.items = []

    def isEmpty(self):
        return self.items == []

    def push(self, item):
        self.items.append(item)

    def pop(self):
        return self.items.pop()

    def peek(self):
        return self.items[-1]

    def size(self):
        return len(self.items) -1


if __name__ == '__main__':
    stack = stack()
    stack.push('first')
    print(stack.isEmpty())
    stack.push('two')
    print(stack.size())
    print(stack.peek())
